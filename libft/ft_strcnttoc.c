#include "libft.h"

int		ft_strcnttoc(char *s, char c)
{
	int i;

	i = 0;
	while (*s != '\0')
	{
		if (*s == c)
			s++;
		if (*s != c)
		{
			i++;
			s++;
		}
	}
	return (i);
}
